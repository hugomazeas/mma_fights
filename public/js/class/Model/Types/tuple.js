class Tuple {
    constructor(item1, item2) {
        this.item1 = item1;
        this.item2 = item2;
    }

    getItem1() {
        return this.item1;
    }

    setItem1(newItem1) {
        this.item1 = newItem1;
    }

    getItem2() {
        return this.item2;
    }

    setItem2(newItem2) {
        this.item2 = newItem2;
    }
}